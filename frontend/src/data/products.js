export const formatPrice = (price) => {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
  }).format(price);
};

export const getDiscountPercent = (original, current) => {
  return Math.round(((original - current) / original) * 100);
};
